class Veiculo:
  # Getters
  def getMarca(self): return self.marca
  def getModelo(self): return self.modelo
  def getCor(self): return self.cor
  def getAno(self): return self.ano
  def getValor(self): return self.valor
  # Setters
  def setMarca(self, marca): self.marca = marca
  def setModelo(self, modelo): self.modelo = modelo
  def setCor(self, cor): self.cor = cor
  def setAno(self, ano): self.ano = ano
  def setValor(self, valor): self.valor = valor
